/**
 * 物件 Object
 * ===============
 * 
 * 物件是以不順序, 鍵值對(key-value pair)來儲存不同的值。
 * 
 * 你可以用一個以字串而成的名字 (i.e. 鍵 key / 屬性 property) 而作儲存值(value)的位置。
 * 
 */


var me = {
    first: "Kyle",
    last: "Simpson",
    age: 39,
    specialties: ["JS", "Table Tennis"]
};

console.log(`My name is ${me.first}.`);